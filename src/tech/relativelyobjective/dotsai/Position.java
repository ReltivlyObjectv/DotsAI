package tech.relativelyobjective.dotsai;

/**
 *
 * @author Christian Russell (me@relativelyobjective.tech)
 */
public class Position {
	public int x,y;
	public Position() {
		x = 1;
		y = 1;
	}
	public Position(int x, int y) {
		this.x = x;
		this.y = y;
	}
	public Position(Position p) {
		x = p.x;
		y = p.y;
	}
}
